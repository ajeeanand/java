package blue.day3.typesvariables;

public class Math {

    public static int max(int x, int y) {
        if (x > y) {
            return x;
        } else {
            return y;
        }
    }
    
    
    public static int min(int x, int y) {
        if (x < y) {
            return x;
        } else {
            return y;
        }
    }
}
